import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import vm from "node:vm";

const rootDir = process.cwd();
const outputDir = path.join(rootDir, "worker", "generated");
const outputFile = path.join(outputDir, "spelling-data.js");

const sandbox = {
  window: {},
  console,
  Math,
  Date,
  crypto: globalThis.crypto,
};

sandbox.window.window = sandbox.window;
sandbox.globalThis = sandbox.window;

const sourceFiles = [
  "vendor/sentence-bank-01.js",
  "vendor/sentence-bank-02.js",
  "vendor/sentence-bank-03.js",
  "vendor/sentence-bank-04.js",
  "vendor/sentence-bank-05.js",
  "vendor/sentence-bank-06.js",
  "vendor/word-list.js",
  "vendor/word-meta.js",
];

for (const relativePath of sourceFiles) {
  const absolutePath = path.join(rootDir, relativePath);
  const source = await readFile(absolutePath, "utf8");
  vm.runInNewContext(source, sandbox, { filename: absolutePath });
}

const words = sandbox.window.KS2_WORDS_ENRICHED || [];
const bySlug = sandbox.window.KS2_WORD_META || {};

await mkdir(outputDir, { recursive: true });
await writeFile(
  outputFile,
  [
    "// Generated by scripts/build-spelling-data.mjs",
    `export const WORDS = ${JSON.stringify(words, null, 2)};`,
    `export const WORD_BY_SLUG = ${JSON.stringify(bySlug, null, 2)};`,
    "",
  ].join("\n"),
  "utf8",
);
